class Missile:
    def __init__(self, id, x, y, speed_x, speed_y, health, endurance, weapons, equipment):
        self.id = id
        self.x = x
        self.y = y
        self.speed_x = speed_x
        self.speed_y = speed_y
        self.health = health
        self.endurance = endurance
        self.weapons = weapons
        self.equipment = equipment
